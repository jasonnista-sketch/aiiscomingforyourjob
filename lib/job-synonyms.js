/**
 * Get search terms for a job slug.
 * Returns the job title (from slug) + any synonyms defined above.
 */
export function getJobSearchTerms(slug) {
  const title = slug.replace(/-/g, " ");
  const synonyms = JOB_SYNONYMS[slug] || [];
  return [title, ...synonyms];
}
